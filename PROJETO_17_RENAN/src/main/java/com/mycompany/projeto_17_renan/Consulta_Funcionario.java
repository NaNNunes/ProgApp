    private void Jbtn_PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_PesquisarActionPerformed
        // TODO add your handling code here:
        Connection connection = null;
        PreparedStatement statement = null;
        String url = "jdbc:mysql://localhost:3306/projeto18";
        String user = "root";
        String psswrd = "";
        
        try {
            connection = DriverManager.getConnection(url, user, psswrd);
            String query = "SELECT nome, sobrenome, CPF, telefone FROM instrutor WHERE id_funcionario = ?";
            statement = connection.prepareStatement(query);
            
            statement.setString(1, Jtxtf_Consulta_FuncionarioByID.getText());
            statement.executeUpdate();
            
            ResultSet resultSet = statement.executeQuery();
            if (resultSet.next()){
                Jtxtf_Retorno_Nome.setText(resultSet.getString("nome") + " " + resultSet.getString("Sobrenome"));
                Jtxtf_Retorno_CPF.setText(resultSet.getString("CPF"));
                Jtxtf_Retorno_Tel.setText(resultSet.getString("telefone"));
                
            }
            else {
                Jtxtf_Retorno_Nome.setText("Instrutor n√£o encontrado!");
            }
            
            System.out.println("Consulta Realizada!");
            
        } catch (SQLException erro){
            System.out.println("Erro: " + erro.getMessage());
        }
    }//GEN-LAST:event_Jbtn_PesquisarActionPerformed

