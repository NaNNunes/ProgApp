    private void Jbtn_queryWhere_raAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_queryWhere_raAlunoActionPerformed
        // TODO add your handling code here:
        Connection connection = null;
        PreparedStatement statement = null;
        
        String url = "jdbc:mysql://localhost:3306/escola";
        String user = "root";
        String psswrd = "";
        
        
        try {
            connection = DriverManager.getConnection(url, user, psswrd);
            String query = "SELECT nome FROM aluno";
            statement = connection.prepareStatement(query);
            
            ResultSet resultSet = statement.executeQuery();
            
            if(resultSet.next()){
                String nome = resultSet.getString("nome");
                Jlbl_retornoNome.setText(nome);
            }
            else {
                Jlbl_retornoNome.setText("Aluno nÃ£o encontrado");
            }
            
            resultSet.close();
            statement.close();
            connection.close();
            
        } catch (SQLException erro) {
            System.out.println("Erro: " + erro);
        }
    }//GEN-LAST:event_Jbtn_queryWhere_raAlunoActionPerformed

