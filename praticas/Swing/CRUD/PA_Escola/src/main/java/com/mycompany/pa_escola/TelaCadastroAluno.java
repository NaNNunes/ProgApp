    private void Jbtn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Jbtn_SalvarActionPerformed
        // TODO add your handling code here:
        Connection connection = null;
        PreparedStatement statement = null;
        
        String url = "jdbc:mysql://localhost:3306/pa_escola";
        String user = "root";
        String passwrd = "";
        
        try{
            connection = DriverManager.getConnection(url, user, passwrd);
            String query = "INSERT INTO aluno(Nome, Sobrenome, CPF, cod_curso) VALUE(?,?,?,?)";
            statement = connection.prepareStatement(query);
            
            statement.setString(1, Jtxtf_CampoNome.getText());
            statement.setString(2, Jtxtf_CampoSobrenome.getText());
            statement.setString(3, Jtxtf_CampoCPF.getText());
            statement.setString(4, Jtxtf_CampoCodCurso.getText());
            statement.executeUpdate();
            System.out.println("Cadastro Realizado");
            
        } catch (SQLException erro){
            System.out.println("Erro: " + erro.getMessage());
        }
    }//GEN-LAST:event_Jbtn_SalvarActionPerformed

